<template>
  <div class="icon-wrap" :style="style">
    <img :src="src" :alt="title || 'icon'" class="w-100">
  </div>
</template>

<script>
export default {
  name: 'Icon',
  props: ['src', 'title', 'styled'],
  computed: {
    style: function () {
      if (this.styled && this.styled.width) {
        return `width: ${this.styled.width};`
      } else {
        return 'width: 120px;'
      }
    }
  }
}
</script>

